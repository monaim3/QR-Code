import type { Metadata } from "next";
import { Suspense } from "react";
import {
  Geist,
  Geist_Mono,
  Rubik,
  Roboto as GoogleRoboto,
} from "next/font/google";
import localFont from "next/font/local";

import "./globals.css";
import ReduxProvider from "@/store/ReduxProvider";
import ModalController from "@/components/modals/modal-controller";

// Toasts
import { Toaster } from "@/components/ui/sonner";
import { ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

/* -------------------- Google Fonts -------------------- */
const geistSans = Geist({
  variable: "--font-geist-sans",
  subsets: ["latin"],
});

const geistMono = Geist_Mono({
  variable: "--font-geist-mono",
  subsets: ["latin"],
});

const rubik = Rubik({
  variable: "--font-rubik",
  subsets: ["latin"],
});

const roboto = GoogleRoboto({
  variable: "--font-roboto",
  subsets: ["latin"],
});

/* -------------------- Local Fonts -------------------- */
const Poppins = localFont({
  src: [
    { path: "../public/fonts/Poppins-Regular.ttf", weight: "400" },
    { path: "../public/fonts/Poppins-Medium.ttf", weight: "500" },
    { path: "../public/fonts/Poppins-SemiBold.ttf", weight: "600" },
    { path: "../public/fonts/Poppins-Bold.ttf", weight: "700" },
  ],
  variable: "--font-poppins",
});

/* -------------------- Metadata -------------------- */
export const metadata: Metadata = {
  title: "SmartQr",
  description: "Generated by create next app",
};

/* -------------------- Root Layout -------------------- */
export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html
      lang="en"
      className={`
        ${Poppins.variable}
        ${geistSans.variable}
        ${geistMono.variable}
        ${roboto.variable}
        ${rubik.variable}
      `}
    >
      <body className="antialiased font-sans">
        <ReduxProvider>
          {children}

          {/* Sonner Toast */}
          <Toaster position="top-right" />

          {/* React Toastify */}
          <ToastContainer position="top-right" />

          {/* modal Controller - wrapped in Suspense for useSearchParams() */}
          <Suspense fallback={null}>
            <ModalController />
          </Suspense>
          
        </ReduxProvider>
      </body>
    </html>
  );
}
